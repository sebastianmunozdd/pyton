import random

# Algoritmo que genera un número aleatorio y lo incrementa en un 30%

# Generar número aleatorio entre 0 y 200
numero = random.randint(0, 200)

# Calcular el aumento del 30%
aumentado = numero * 1.30

# Mostrar resultados
print("Número generado:", numero)
print("Número aumentado en un 30%:", aumentado)