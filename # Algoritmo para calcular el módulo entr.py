# Algoritmo para calcular el módulo entre dos números

# Entrada de datos
numero1 = int(input("Introduce el primer número: "))
numero2 = int(input("Introduce el segundo número: "))

# Proceso: calcular el módulo
modulo = numero1 % numero2

# Salida de datos
print("El módulo de", numero1, "y", numero2, "es:", modulo)